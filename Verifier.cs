namespace knightmoves;
public class Verifier
{

    
    public string OldEnough(int age){
        if (age > 17) {
            return "old enough";
        } else {
            return "";
        }
    }

    
    public string UnderAge(int age){
        if (age < 18) {
            return "under age";
        } else {
            return "";
        }
    }

    
    public int LegalSpeed(int speed){
        if (speed <= 50) {
            return "legal speed";
        } else {
            return "";
        }
    }

    
    public string IsGoldenYears(int age){
        if (age >= 65) {
            return "golden years";
        } else {
            return "";
        }
    }
}



